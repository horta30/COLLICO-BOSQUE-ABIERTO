function haversine(lat1, lon1, lat2, lon2) {
  const R = 6371000;
  const phi1 = lat1 * Math.PI / 180, phi2 = lat2 * Math.PI / 180;
  const dphi = (lat2 - lat1) * Math.PI / 180;
  const dlambda = (lon2 - lon1) * Math.PI / 180;
  const a = Math.sin(dphi/2)**2 + Math.cos(phi1) * Math.cos(phi2) * Math.sin(dlambda/2)**2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

function checkProximity(userLat, userLng) {
  const alerts = [];
  for (const s of SENDEROS) {
    if (!s.geofence) continue;
    const dInicio = haversine(userLat, userLng, s.geofence.inicio.lat, s.geofence.inicio.lng);
    const dFin    = haversine(userLat, userLng, s.geofence.fin.lat,    s.geofence.fin.lng);
    if (dInicio <= s.geofence.inicio.radio) alerts.push({ sendero: s, tipo: 'inicio', distancia: Math.round(dInicio) });
    if (dFin    <= s.geofence.fin.radio)    alerts.push({ sendero: s, tipo: 'fin',    distancia: Math.round(dFin) });
  }
  return alerts;
}

