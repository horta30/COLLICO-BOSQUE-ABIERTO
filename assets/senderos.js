function getSubsegmentos(parentId) {
  return SUBSEGMENTOS.filter(sub => sub.padre === parentId);
}

// Coords de un sub-segmento (slice del padre, invertir si corresponde)
function getSubsegmentoCoords(subId) {
  const sub = SUBSEGMENTOS.find(s => s.id === subId);
  if (!sub) return [];
  const padre = getSenderoById(sub.padre);
  if (!padre) return [];
  let coords = padre.coords.slice(sub.start_idx, sub.end_idx + 1);
  if (sub.invertir) coords = coords.slice().reverse();
  return coords;
}

// Coords truncadas hasta cierto índice (para tramos 'acceso' en RUTAS)
function getCoordsHastaIdx(senderoId, idx) {
  const s = getSenderoById(senderoId);
  if (!s) return [];
  return s.coords.slice(0, idx + 1);
}

// GeoJSON de una ruta completa (planificador)
function buildRutaGeoJSON(rutaId) {
  const ruta = RUTAS[rutaId];
  if (!ruta) return null;
  const features = [];
  ruta.pasos.forEach((paso, i) => {
    let coords = [];
    if (paso.tipo === 'ripio') {
      coords = RIPIO_ACCESO;
    } else if (paso.tipo === 'sendero') {
      const s = getSenderoById(paso.id);
      if (s) coords = s.coords;
    } else if (paso.tipo === 'acceso') {
      coords = getCoordsHastaIdx(paso.id, paso.hasta_idx);
    }
    if (coords.length > 1) {
      features.push({
        type: 'Feature',
        properties: { label: paso.label, color: paso.color, paso: i },
        geometry: { type: 'LineString', coordinates: coords }
      });
    }
  });
  return { type: 'FeatureCollection', features };
}

// Categoría de dificultad según D+/km (ITRA)
function categoriaDificultad(dpkm) {
  if (dpkm <  30) return 'Fácil';
  if (dpkm <  90) return 'Moderado';
  if (dpkm < 120) return 'Difícil';
  return 'Muy Difícil';
}

